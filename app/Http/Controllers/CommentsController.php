     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {

        $comment= Comment::find($id);
        if(!$comment){
            return redirect()->to('/articles');
        }
        $comment->delete();
        return redirect()->route('articles.index');

